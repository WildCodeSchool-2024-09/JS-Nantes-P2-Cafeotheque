// Import necessary modules from React and React Router
import { StrictMode } from "react";
import { createRoot } from "react-dom/client";
import { RouterProvider, createBrowserRouter } from "react-router-dom";

/* ************************************************************************* */

// Import the main app component
import App from "./App";

// Import additional components for new routes

import CoffeeDetailPage from "./pages/CoffeeDetailPage/CoffeeDetailPage";
import CoffeesPage from "./pages/CoffeesPage";
import HistoryOfCoffeePage from "./pages/HistoryPage";
import HomePage from "./pages/HomePage";
import LoginPage from "./pages/LoginPage";
import ProfilePage from "./pages/ProfilePage";
import RecipeePage from "./pages/RecipeePage";
import RegisterPage from "./pages/RegisterPage";
import WorldCoffeePage from "./pages/WorldCoffeePage";

/* ************************************************************************* */

// Create router configuration with routes
// You can add more routes as you build out your app!
const router = createBrowserRouter([
  {
    element: <App />,
    children: [
      {
        path: "/",
        element: <HomePage />,
      },
      {
        path: "/coffees",
        element: <CoffeesPage />,
      },
      {
        path: "/coffee/:id",
        element: <CoffeeDetailPage />,
      },
      {
        path: "/login",
        element: <LoginPage />,
      },
      {
        path: "/register",
        element: <RegisterPage />,
      },
      {
        path: "/profile",
        element: <ProfilePage />,
      },
      {
        path: "/recipes",
        element: <RecipeePage />,
      },
      {
        path: "/world-coffees",
        element: <WorldCoffeePage />,
      },
      {
        path: "/the-history-of-coffee",
        element: <HistoryOfCoffeePage />,
      },
    ],
  },
  // Try adding a new route! For example, "/about" with an About component
]);

/* ************************************************************************* */

// Find the root element in the HTML document
const rootElement = document.getElementById("root");
if (rootElement == null) {
  throw new Error(`Your HTML Document should contain a <div id="root"></div>`);
}

// Render the app inside the root element
createRoot(rootElement).render(
  <StrictMode>
    <RouterProvider router={router} />
  </StrictMode>,
);

